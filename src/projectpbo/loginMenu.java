    private void loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginActionPerformed
        // TODO add your handling code here:
        try {
            final String select = "SELECT * FROM user WHERE username='"+getUsername().getText()+"' AND password='"+getPassword().getText()+"'";
            Statement st = connection.createStatement();
            ResultSet rs = st.executeQuery(select);
            if(rs.next()){
                if(getUsername().getText().equals(rs.getString("username")) && getPassword().getText().equals(rs.getString("password"))){
                    JOptionPane.showMessageDialog(this, "berhasil login");
                    if("admin".equals(rs.getString("role"))){
                        dispose();
                        adminPetugas menu = new adminPetugas();
                        menu.setVisible(true);
                        menu.setLocationRelativeTo(null);
                    }else{
                        dispose();
                        petugasBuat menu = new petugasBuat();
                        menu.setVisible(true);
                        menu.setLocationRelativeTo(null);
                    }
                }
            }else{
                    JOptionPane.showMessageDialog(this, "username atau password salah");
                }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_loginActionPerformed

